function calculete (n1, n2) {
    let total;
    function adicao () {
        const somar = (n1 + n2)
        total = somar
        return total
    }
    function subtracao () {
        const menos = (n1 - n2)
        total = menos
        return total
    }
    function multiple () {
        const mult = (n1 * n2)
        total = mult
        return total
    }
    function divisor () {
        const div = (n1 / n2)
        total = div
        return total
    }
}
